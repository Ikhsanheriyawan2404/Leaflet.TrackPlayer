map.fitBounds(path);
let track = new L.TrackPlayer(path, {
  markerIcon: L.icon({
    iconSize: [27, 54],
    iconUrl: "./lib/assets/car.png",
    iconAnchor: [13.5, 27],
  }),
  panTo:true,
  speed: 500,
});
track.addTo(map);
let control = {
  speed: track.options.speed,
  progress: track.options.progress * 100,
  start: function () {
    map.setZoom(17, {
      animate: false,
    });
    track.start();
  },
  pause: function () {
    track.pause();
  },
  status: "original",
  carLatLng: "original",
};
let gui = new dat.GUI({
  width: 500,
});
track.on("start", () => {
  console.log("start");
  control.status = "start";
});
track.on("pause", () => {
  console.log("pause");
  control.status = "pause";
});
track.on("finished", () => {
  console.log("finished");
  control.status = "finished";
});
track.on("progress", (progress, { lng, lat }) => {
  console.log("moving");
  control.carLatLng = `${lng},${lat}`;
  control.progress = progress * 100;
  control.status = "moving";
});

gui.add(control, "start");
gui.add(control, "pause");

let timeout = null;
gui
  .add(control, "speed", 300, 5000)
  .onChange((e) => {
    //函数防抖，根据实际需求决定是否添加
    clearTimeout(timeout);
    timeout = setTimeout(() => {
      track.setSpeed(e);
    }, 20);
  })
  .name("speed(km/h)");
gui.add(control, "status").listen();
gui.add(control, "carLatLng").listen();
gui
  .add(control, "progress", 0, 100)
  .onChange((e) => {
    track.setProgress(e / 100);
  })
  .name("progress")
  .listen();
